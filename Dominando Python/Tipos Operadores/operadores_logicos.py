##Operadores and

#Sempre que o operador lógico for o and, o valor retornado será True se todos também tiverem o valor como True.
#Se algum dos valores comparados for False, consequentemente o resultado também sera um booleano False

print(True and True)
print(True and False)
print(False and False)

## Operadores Or

#Caso o operador logico seja o or, o valor retornado sera True se um dos valores tambem for True

print(True or True)
print(True or False)
print(False or False)
